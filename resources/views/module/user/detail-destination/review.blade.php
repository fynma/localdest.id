                    '<button class="absolute top-0 right-0 bg-red-500 text-white rounded-full p-1"><svg class="w-4 h-4" fill="currentColor" viewBox="0 0 20 20"><path fill-rule="evenodd" d="M2.293 2.293a1 1 0 011.414 0L10 8.586l6.293-6.293a1 1 0 111.414 1.414L11.414 10l6.293 6.293a1 1 0 01-1.414 1.414L10 11.414l-6.293 6.293a1 1 0 01-1.414-1.414L8.586 10 2.293 3.707a1 1 0 010-1.414z" clip-rule="evenodd"></path></svg></button>'
                ); // Buat tombol silang

                // Tambahkan event listener untuk menghapus gambar saat tombol silang diklik
                closeButton.click(function() {
                    imageContainer.remove(); // Hapus kontainer gambar dan tombol silang
                    var fileNameToRemove = image.attr('src').split('/')
                        .pop(); // Ambil nama file gambar yang akan dihapus
                    // Dapatkan indeks file yang akan dihapus dari daftar file
                    var indexToRemove = Array.from($('#file-input')[0].files).findIndex(file => file
                        .name === fileNameToRemove);
                    // Hapus file dari daftar file
                    if (indexToRemove !== -1) {
                        var newFiles = Array.from($('#file-input')[0].files);
                        newFiles.splice(indexToRemove, 1);
                        $('#file-input')[0].files = new FileList(newFiles, $('#file-input')[0].files.name);
                    }
                });

                imageContainer.append(image); // Tambahkan gambar ke dalam kontainer
                imageContainer.append(closeButton); // Tambahkan tombol silang ke dalam kontainer
                preview.append(imageContainer); // Tambahkan kontainer ke dalam kontainer preview
            }

            reader.readAsDataURL(file); // Baca file gambar sebagai URL data
        }

    }
</script>
